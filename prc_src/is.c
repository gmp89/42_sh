int		is_space(char c)
{
	return (c == ' ' || c == '\t');
}

int		is_op(char c)
{
	if (c == '>' || c == '<' || c == '&' || c == '|' ||
		c == '(' || c == ')' || c == ';')
		return (1);
	else
		return (0);
}
