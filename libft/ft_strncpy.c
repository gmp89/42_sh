#include "libft.h"

char		*ft_strncpy(char *s1, const char *s2, size_t n)
{
	size_t		i;
	size_t		j;

	i = 0;
	j = 0;
	while (i < n)
	{
		if (s2[j] == '\0')
		{
			s1[i] = '\0';
			i++;
		}
		else
		{
			s1[i] = s2[j];
			i++;
			j++;
		}
	}
	return (s1);
}
